package main

import (
	"fmt"
)

func main() {
	fmt.Println("Switch Example")
	num := 1
	switch num {
		
		case 1:
		fmt.Println("Wound up in case number 1")
		
		case 2:
		fmt.Println("Wound up in case number 2")
		
		case 3:
		fmt.Println("Wound up in case number 3")
	
	}
	
	fmt.Println("I am at the end of this program")
	
}
